
package Lista_Desafio;

import java.util.Scanner;

public class Desafio_26 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        
        int contador = 100;
        
        while(contador>0){
            
            System.out.println(contador);
            
            contador--;
        }
        sc.close();
    }
}
